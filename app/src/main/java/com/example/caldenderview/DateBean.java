package com.example.caldenderview;

public class DateBean {
    int year;
    int month;
    int day;

    public DateBean(int year, int month, int day) {
        this.year = year;
        this.month = month;
        this.day = day;

    }

    public int getYear() {
        return year;
    }

    public int getMonth() {
        return month;
    }

    public int getDay() {
        return day;
    }


}
